#!/bin/bash
# Еженедельный SEO-анализ через Claude + PR-CY MCP
# Запускается cron каждое воскресенье в 09:00

set -e

SEO_DIR="/Users/shuvaev/Продукты/unilist/saas-workspace/marketing/SEO"
DATE=$(date +%Y-%m-%d)
REPORT_DIR="$SEO_DIR/reports/$DATE"

mkdir -p "$REPORT_DIR"

cd "$SEO_DIR"

echo "[$(date)] Запуск SEO-анализа..." >> /tmp/seo-pipeline.log

claude --dangerously-skip-permissions -p "
Ты — SEO-аналитик. Используй инструменты PR-CY MCP для еженедельного анализа конкурентов сайта unilist.ru.

## Задача

1. **Ключевые слова** — для каждого seed keyword получи данные через get_wordstat:
   - опросный лист продажи
   - B2B анкета клиента
   - квалификация лидов форма
   - форма для клиента Bitrix24
   - опросник менеджера по продажам

2. **Конкуренты** — для топ-5 ключевых слов (по объёму) получи выдачу через get_serp (se=yandex), найди домены-конкуренты (исключи unilist.ru).

3. **Позиции** — проверь текущие позиции unilist.ru через get_position для ключевых слов с наибольшим объёмом.

## Результат

Запиши три файла в папку $REPORT_DIR/:

**keywords.md** — таблица всех найденных ключей с объёмами, сгруппированных по кластерам.

**competitors.md** — по каждому конкуренту: домен, топ-страницы, контент-стратегия (какие типы страниц: сравнения/интеграции/гайды/ниши).

**action-plan.md** — приоритизированный план страниц для unilist.ru:
- Приоритет 1 (создать за 2 недели): высокий объём + слабые конкуренты + нас нет в топ-20
- Приоритет 2 (следующий месяц)
- Приоритет 3 (следующий квартал)

Для каждой рекомендованной страницы укажи: URL-slug, тип страницы, целевой кластер, объём, обоснование приоритета.

Пиши файлы на русском языке.
" >> /tmp/seo-pipeline.log 2>&1

echo "[$(date)] Готово. Отчёты в $REPORT_DIR/" >> /tmp/seo-pipeline.log
